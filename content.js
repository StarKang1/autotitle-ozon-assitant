// 过滤中文字符、标签和替换特殊符号
function removeChineseCharacters(text) {
  return text
    .replace(/[\u4e00-\u9fa5]/g, '') // 过滤中文字符
    .replace(/#[^\s#]+/g, '') // 过滤标签
    .replace(/—/g, '-') // 长破折号换成短破折号
    .replace(/«/g, '<') // 左双引号换成小于号
    .replace(/»/g, '>') // 右双引号换成大于号
    .replace(/\s+/g, ' ') // 合并多个空格为单个空格
    .trim();
}

// 规范化标题文本，确保只取第一行有效标题
function normalizeTitleText(text) {
  if (!text) {
    return '';
  }
  const lines = String(text)
    .split('\n')
    .map((line) => line.trim())
    .filter(Boolean);
  const firstLine = lines.length > 0 ? lines[0] : String(text).trim();
  const cleaned = removeChineseCharacters(firstLine);
  const uniqueTitle = removeDuplicateWords(cleaned);
  return formatRussianTitle(uniqueTitle);
}

// 移除标题中的重复词
function removeDuplicateWords(text) {
  if (!text) {
    return '';
  }
  
  // 按空格分割单词
  const words = text.split(/\s+/);
  const uniqueWords = [];
  const seen = new Set();
  
  // 遍历单词，只添加第一次出现的单词
  for (const word of words) {
    if (!seen.has(word)) {
      seen.add(word);
      uniqueWords.push(word);
    }
  }
  
  return uniqueWords.join(' ');
}

// 根据关键词对俄语标题做轻量断句（逗号/连词）
function formatRussianTitle(title) {
  if (!title) {
    return '';
  }
  let result = title
    .replace(/\s+/g, ' ')
    .replace(/\s+,/g, ',')
    .replace(/,\s*,/g, ',')
    .trim();

  // 处理游戏平台名称的分隔
  const platforms = [
    'Switch Pro',
    'PS5',
    'PS4',
    'Xbox',
    'Xbox One',
    'Xbox Series X',
    'PS3',
    'Nintendo Switch',
    'PC'
  ];
  
  // 为平台名称之间添加逗号
  let platformPattern = '';
  platforms.forEach((platform, index) => {
    if (index > 0) platformPattern += '|';
    platformPattern += platform.replace(/[+.]/g, '\\$&');
  });
  
  const platformRegex = new RegExp(`(${platformPattern})\s+(${platformPattern})`, 'gi');
  result = result.replace(platformRegex, '$1, $2');

  // 在常见分隔短语前加逗号，避免一长串
  const commaBefore = [
    ' украшение',
    ' декор',
    ' тематическая вечеринка',
    ' для дома',
    ' для семьи',
    ' карнавал',
    ' хэллоуин',
    ' косплей',
    ' детский костюм',
    ' для фотосессии',
    ' жесткий',
    ' портативный',
    ' защитный',
    ' пластиковый',
    ' металлический',
    ' качественный',
    ' недорогой',
    ' высокого качества'
  ];

  // 为连续的形容词添加逗号
  let adjectivePattern = '';
  const adjectives = [
    'жесткий',
    'портативный',
    'защитный',
    'пластиковый',
    'металлический',
    'качественный',
    'недорогой',
    'высокого качества'
  ];
  
  adjectives.forEach((adj, index) => {
    if (index > 0) adjectivePattern += '|';
    adjectivePattern += adj;
  });
  
  const adjectiveRegex = new RegExp(`(${adjectivePattern})\s+(${adjectivePattern})`, 'gi');
  result = result.replace(adjectiveRegex, '$1, $2');

  commaBefore.forEach((phrase) => {
    const re = new RegExp(`\\s${phrase}`, 'gi');
    result = result.replace(re, `,${phrase}`);
  });

  // 处理“для ... и ...”的连词一致性
  result = result.replace(/,? для дня рождения тематическая вечеринка/gi, ', для дня рождения и тематической вечеринки');
  result = result.replace(/,? карнавал хэллоуин косплей/gi, ', карнавал, хэллоуин, косплей');

  // 确保逗号后面有空格
  result = result.replace(/,([^\s])/g, ', $1');
  // 移除多余的逗号
  result = result.replace(/,\s*,/g, ',').replace(/\s+,/g, ',').trim();
  // 移除开头的逗号
  result = result.replace(/^,\s*/, '');

  return result;
}


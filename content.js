// 生成缓存键的哈希函数
function generateCacheKey(prompt) {
  let hash = 0;
  for (let i = 0; i < prompt.length; i++) {
    const char = prompt.charCodeAt(i);
    hash = ((hash << 5) - hash) + char;
    hash = hash & hash;
  }
  return 'qwen_ai_cache_' + hash.toString(36);
}

// 检查缓存
function checkCache(prompt) {
  const cacheKey = generateCacheKey(prompt);
  const cached = localStorage.getItem(cacheKey);
  if (cached) {
    try {
      const parsed = JSON.parse(cached);
      const now = Date.now();
      // 缓存有效期为24小时
      if (now - parsed.timestamp < 24 * 60 * 60 * 1000) {
        console.log('使用缓存的 AI 响应');
        return parsed.data;
      } else {
        // 缓存过期，删除
        localStorage.removeItem(cacheKey);
      }
    } catch (error) {
      console.error('解析缓存数据失败:', error);
      localStorage.removeItem(cacheKey);
    }
  }
  return null;
}

// 更新缓存
function updateCache(prompt, data) {
  const cacheKey = generateCacheKey(prompt);
  const cacheData = {
    timestamp: Date.now(),
    data: data
  };
  localStorage.setItem(cacheKey, JSON.stringify(cacheData));
  console.log('AI 响应已缓存');
}

// 调用 Qwen AI 接口（通过后台脚本）
async function callQwenAI(prompt) {
  // 检查缓存
  const cachedData = checkCache(prompt);
  if (cachedData) {
    return cachedData;
  }

  return new Promise((resolve, reject) => {
    console.log('通过后台脚本调用 Qwen AI，提示词:', prompt.substring(0, 100) + '...');
    
    // 检查插件上下文是否有效
    if (!chrome.runtime || !chrome.runtime.sendMessage) {
      console.error('插件上下文无效，可能已被重新加载');
      reject(new Error('插件上下文无效，请刷新页面后重试'));
      return;
    }
    
    try {
      // 向后台脚本发送消息
      chrome.runtime.sendMessage(
        { action: 'callQwenAI', prompt },
        (response) => {
          console.log('收到后台脚本的响应:', response);
          
          if (chrome.runtime.lastError) {
            console.error('后台脚本通信错误:', chrome.runtime.lastError);
            // 检查是否是上下文无效错误
            if (chrome.runtime.lastError.message.includes('Extension context invalidated')) {
              reject(new Error('插件上下文无效，请刷新页面后重试'));
            } else {
              reject(new Error('与后台脚本通信失败'));
            }
            return;
          }
          
          if (response.success) {
            // 更新缓存
            updateCache(prompt, response.result);
            resolve(response.result);
          } else {
            reject(new Error(response.error || 'Qwen AI 调用失败'));
          }
        }
      );
    } catch (error) {
      console.error('发送消息时出错:', error);
      if (error.message.includes('Extension context invalidated')) {
        reject(new Error('插件上下文无效，请刷新页面后重试'));
      } else {
        reject(new Error('调用 AI 时出错: ' + error.message));
      }
    }
  });
}


// 读取 DOM 元素
async function readDOM() {
  // 刷新DOM缓存
  if (!isCacheValid()) {
    refreshDOMCache();
  }

  // 读取关键字/标签
  let keywords = '';
  if (DOMCache.tagsInput && DOMCache.tagsInput.value) {
    keywords = DOMCache.tagsInput.value;
  }

  // 如果找不到关键字字段，尝试从其他地方获取
  if (!keywords) {
    // 尝试从标题中获取关键字
    if (DOMCache.titleInput && DOMCache.titleInput.value) {
      keywords = DOMCache.titleInput.value;
      console.log('从标题中获取关键字:', keywords);
    } else {
      // 使用默认关键字
      keywords = '默认关键字';
      console.log('使用默认关键字:', keywords);
    }
  }

  // 读取产品标题
  const productTitle = DOMCache.titleInput ? DOMCache.titleInput.value : '';

  // 读取产品描述
  let productDescription = '';
  DOMCache.descriptionElements.forEach((element) => {
    if (element.value || element.innerText) {
      productDescription = element.value || element.innerText;
    }
  });

  // 读取选中的属性（推荐）
  const attributes = Array.from(DOMCache.checkedCheckboxes).map((checkbox) => checkbox.value);

  // 读取商品来源链接（可选）
  const sourceUrl = DOMCache.sourceUrlInput ? DOMCache.sourceUrlInput.value : '';
  
  // 读取颜色名称（Название цвета / 颜色名称）
  let colorName = '';
  if (DOMCache.colorInputs.length === 0) {
    // 颜色输入可能延迟渲染，等待后再试
    await new Promise((resolve) => setTimeout(resolve, 300)); // 减少等待时间
    DOMCache.colorInputs = findColorNameInputs();
  }
  if (DOMCache.colorInputs.length > 0) {
    const firstInput = DOMCache.colorInputs[0];
    colorName = 'value' in firstInput ? firstInput.value : firstInput.innerText;
    console.log('读取到颜色名称:', colorName);
  }

  return { keywords, productTitle, productDescription, attributes, sourceUrl, colorName };
}

// Qwen AI 授权管理
class QwenAuth {
  constructor() {
    this.authUrl = CONFIG.qwen.authUrl;
    this.tokenKey = CONFIG.qwen.tokenKey;
  }

  async authorize() {
    return new Promise((resolve, reject) => {
      console.log('开始 Qwen AI 授权流程');
      
      // 检查插件上下文是否有效
      if (!chrome.runtime || !chrome.runtime.sendMessage) {
        console.error('插件上下文无效，可能已被重新加载');
        reject(new Error('插件上下文无效，请刷新页面后重试'));
        return;
      }
      
      // 检查是否已有授权令牌
      const existingToken = localStorage.getItem(this.tokenKey);
      if (existingToken) {
        console.log('使用现有授权令牌');
        resolve(existingToken);
        return;
      }

      console.log('打开授权窗口:', this.authUrl);
      
      // 打开授权窗口
      const authWindow = window.open(this.authUrl, 'QwenAuth', 'width=800,height=600');
      if (!authWindow) {
        const errorMsg = '无法打开授权窗口，请检查浏览器弹窗设置';
        console.error(errorMsg);
        reject(new Error(errorMsg));
        return;
      }

      console.log('授权窗口已打开，等待用户操作');

      // 监听授权结果
      const checkAuth = setInterval(() => {
        try {
          // 检查插件上下文是否有效
          if (!chrome.runtime || !chrome.runtime.sendMessage) {
            clearInterval(checkAuth);
            console.error('插件上下文无效，可能已被重新加载');
            reject(new Error('插件上下文无效，请刷新页面后重试'));
            return;
          }
          
          if (authWindow.closed) {
            clearInterval(checkAuth);
            console.log('授权窗口已关闭');
            
            // 检查插件上下文是否有效
            if (!chrome.runtime || !chrome.runtime.sendMessage) {
              console.error('插件上下文无效，可能已被重新加载');
              reject(new Error('插件上下文无效，请刷新页面后重试'));
              return;
            }
            
            // 检查是否在授权后存储了令牌
            const token = localStorage.getItem(this.tokenKey);
            if (token) {
              console.log('授权成功，获取到令牌');
              resolve(token);
            } else {
              if (CONFIG.useMockOnError) {
                // 即使没有令牌，也生成临时令牌以保证功能可用
                console.log('未获取到令牌，使用临时令牌');
                const tempToken = 'temp_qwen_token_' + Date.now();
                this.setToken(tempToken);
                resolve(tempToken);
              } else {
                reject(new Error('未获取到授权令牌'));
              }
            }
          }
        } catch (error) {
          // 跨域错误是正常的，忽略
          if (!error.message.includes('Extension context invalidated')) {
            console.log('授权窗口检查时的跨域错误（正常）:', error.message);
          } else {
            // 插件上下文无效错误，需要处理
            clearInterval(checkAuth);
            console.error('插件上下文无效，可能已被重新加载');
            reject(new Error('插件上下文无效，请刷新页面后重试'));
          }
        }
      }, 1000);
    });
  }

  getToken() {
    return localStorage.getItem(this.tokenKey);
  }

  setToken(token) {
    localStorage.setItem(this.tokenKey, token);
  }

  clearToken() {
    localStorage.removeItem(this.tokenKey);
  }
}


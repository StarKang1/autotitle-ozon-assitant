function truncateChineseTo(text, maxChinese) {
  if (!text) {
    return '';
  }
  let count = 0;
  let result = '';
  for (const ch of String(text)) {
    if (/[\u4e00-\u9fa5]/.test(ch)) {
      count += 1;
      if (count > maxChinese) {
        break;
      }
    }
    result += ch;
  }
  return result;
}


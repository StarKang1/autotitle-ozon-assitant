// 回车触发生成（优化版，避免干扰其他功能）
function addEnterKeyTrigger() {
  const handler = (event) => {
    // 检查是否在输入状态
    if (event.isComposing) {
      return;
    }
    
    // 检查是否是纯 Enter 键（无修饰键）
    if (event.key === 'Enter' && !event.shiftKey && !event.ctrlKey && !event.altKey && !event.metaKey) {
      const target = event.target;
      const tag = target && target.tagName ? target.tagName.toLowerCase() : '';
      
      // 检查是否在输入框中
      const isInput = tag === 'input' || tag === 'textarea' || (target && target.getAttribute && target.getAttribute('contenteditable') === 'true');
      if (isInput) {
        return;
      }
      
      // 检查是否在弹出窗口中
      const inModal = target.closest('.ant-modal, [role="dialog"]');
      if (inModal) {
        return;
      }
      
      // 检查是否在重量输入相关区域
      const inWeightArea = target.closest('[data-name*="weight"], [class*="weight"], [id*="weight"]');
      if (inWeightArea) {
        return;
      }
      
      // 移除事件监听器并触发生成
      document.removeEventListener('keydown', handler);
      manualGenerateAll();
    }
  };
  
  // 添加事件监听器
  document.addEventListener('keydown', handler);
}


function simulateInputText(element, value) {
  element.focus();
  if ('value' in element) {
    const setter = Object.getOwnPropertyDescriptor(element.__proto__, 'value')?.set;
    if (setter) {
      setter.call(element, '');
      element.dispatchEvent(new Event('input', { bubbles: true }));
      setter.call(element, value);
    } else {
      element.value = '';
      element.dispatchEvent(new Event('input', { bubbles: true }));
      element.value = value;
    }
  } else {
    element.innerText = '';
    element.dispatchEvent(new Event('input', { bubbles: true }));
    element.innerText = value;
  }
  element.dispatchEvent(new Event('input', { bubbles: true }));
  element.dispatchEvent(new Event('change', { bubbles: true }));
}

function truncateChineseTo(text, maxChinese) {
  if (!text) {
    return '';
  }
  let count = 0;
  let result = '';
  for (const ch of String(text)) {
    if (/[\u4e00-\u9fa5]/.test(ch)) {
      count += 1;
      if (count > maxChinese) {
        break;
      }
    }
    result += ch;
  }
  return result;
}

